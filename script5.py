mapa = [
    ["*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*"],
    ["*", " ", "*", " ", " ", " ", " ", " ", " ", " ", " ", " ", " ", " ", "*"],
    ["*", " ", "*", " ", " ", " ", " ", " ", " ", " ", " ", " ", " ", " ", "*"],
    ["*", " ", "*", " ", " ", " ", "*", " ", " ", " ", "*", " ", " ", " ", "*"],
    ["*", " ", " ", " ", "*", "*", "*", " ", " ", " ", "*", " ", " ", " ", "*"],
    ["*", " ", " ", " ", "*", " ", " ", " ", " ", "*", "*", "*", " ", " ", "*"],
    ["*", " ", " ", " ", "*", " ", " ", " ", " ", " ", "*", " ", " ", " ", "*"],
    ["*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*", "*"]
]

# Початкова позиція гравця
playerX = 1
playerY = 1


# Функція для виведення карти
def show_map():
    # Створюємо копію карти
    temp_map = [row[:] for row in mapa]
    # Розміщуємо гравця
    temp_map[playerY][playerX] = "P"
    # Виводимо карту
    for row in temp_map:
        print(*row)


# Головний ігровий цикл
while True:
    show_map()
    print("\nКеруй: w(вгору), s(вниз), a(вліво), d(вправо), q(вихід)")

    move = input("Твій хід: ").lower()

    # Зберігаємо стару позицію
    newX = playerX
    newY = playerY

    # Обробка руху
    if move == 'w':
        newY -= 1
    elif move == 's':
        newY += 1
    elif move == 'a':
        newX -= 1
    elif move == 'd':
        newX += 1
    elif move == 'q':
        print("Гра завершена!")
        break

    # Перевірка чи можна рухатись (не стіна)
    if mapa[newY][newX] != "*":
        playerX = newX
        playerY = newY
    else:
        print("Не можна йти через стіну!")